            ELSE 'grams'::quantity_enum
        END as quantity_type,
        CASE 
            WHEN ing_id = 11 THEN 'grain'
            WHEN ing_id BETWEEN 39 AND 48 THEN 'spice'
            ELSE 'vegetable' 
        END as category
    FROM ingredient
) AS prepared_data
CROSS JOIN generate_series(1,5)
ON CONFLICT DO NOTHING;


