    (random() * 49 + 1)::integer,
    ing_id,
    CASE 
        WHEN quantity_type = 'milliliters' THEN (random() * 900 + 100)::integer 
        WHEN quantity_type = 'pieces' THEN (random() * 4 + 1)::integer
        ELSE CASE
            WHEN category = 'spice' THEN (random() * 450 + 50)::integer
