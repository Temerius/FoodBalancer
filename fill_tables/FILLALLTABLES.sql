    SELECT 
        ing_id,
        CASE 
            WHEN ing_id IN (31,32,33,34,35,36,37,38,42) THEN 'pieces'::quantity_enum
            WHEN ing_id IN (49,50) THEN 'milliliters'::quantity_enum
